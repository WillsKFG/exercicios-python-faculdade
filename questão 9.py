
#9 - calculo da média aritmética

print("Vamos calcular sua média aritmética com 3 valores.")

n1 = float(input("Digite seu primeiro valor: "))
n2 = float(input("Digite seu segundo valor: "))
n3 = float(input("Digite seu terceiro valor: "))

media = (n1 + n2 + n3) / 3

print("Sua média aritmética é: ", media)